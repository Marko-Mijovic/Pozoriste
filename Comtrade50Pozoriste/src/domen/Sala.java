package domen;

public class Sala {
	
	private int idSale;
	private String nazivSale;
	private int kapacitetSale;
	private int idPozorista;
	public int getIdSale() {
		return idSale;
	}
	public void setIdSale(int idSale) {
		this.idSale = idSale;
	}
	public String getNazivSale() {
		return nazivSale;
	}
	public void setNazivSale(String nazivSale) {
		this.nazivSale = nazivSale;
	}
	public int getKapacitetSale() {
		return kapacitetSale;
	}
	public void setKapacitetSale(int kapacitetSale) {
		this.kapacitetSale = kapacitetSale;
	}
	public int getIdPozorista() {
		return idPozorista;
	}
	public void setIdPozorista(int idPozorista) {
		this.idPozorista = idPozorista;
	}
	
	

}
